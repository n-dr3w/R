#Zwracam Państwa uwagę, że for z przykładu powyżej zachowuje się inaczej niż while. Dlaczego?
  
 # Pętla While jest pętlą w R, która kontynuuje wykonywanie bloku instrukcji, dopóki nie zostanie spełniony określony warunek. 

 # Pętla For to pętla w R, która wykonuje się przez określoną liczbę razy, wykonując określony blok instrukcji w każdej iteracji. 